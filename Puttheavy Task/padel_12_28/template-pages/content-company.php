        <h3><span>MESSAGE</span><span>代表メッセージ</span></h3>
        <div class="companyContent">
          <h4><span>代表挨拶</span></h4>
          <p>2015年春。奇跡的な出会い、そして転機が訪れました。知合いから「パデルっていうテニスに似た面白いスポーツがあるから遊びにいきませんか？南米風BBQ食べながら、ビール飲みながら楽しめるイベントがあるんです。」と誘われ、家から遠い場所だったので最初は乗り気でなく「BBQしてビールが飲めればいいや」という軽い気持ちでパデルのイベントに参加しました。</p>
          <p>実際にパデルをプレイをして、観戦して、BBQでお肉を食べて、、、</p>
          <ul>
            <li>・プレイしている人と観客の一体感、</li>
            <li>・初心者でも上級者と一緒に試合が楽しめる競技性</li>
            <li>・エキサイティングなラリーが続くゲーム性</li>
          </ul>
          <p>気が付けば完全に「パデル」の虜になっていました。</p>
          <p>そこからは、寝ても覚めてもパデルのことばかり・・・。</p>
          <p>パデルを知れば知るほど、私の中のパデル愛は急激に大きくなっていきました。「この素晴らしいスポーツを一人でも多くの人に知ってもらいプレイしてもらいたい。」</p>
          <p>そしてパデルと出会ってから4ヶ月。</p>
          <p>14年間経営してきたIT会社の社長を退任し、これからの人生をパデルに捧げることを決めました。</p>
          <p>その後は、東京初となるパデル施設のオープンを目標として、クラウドファンディングで資金を集め、第一号施設となるパデル東京を2016年12月3日にオープンすることができました。</p>
          <p>パデル東京には年間8000人の方がご来場くださり、また多くの方のご支援、ご協力により、初年度から黒字化を達成することができ、スポーツとしての認知とともにビジネスとしての可能性も見出すことができました</p>
          <p>2018年には新たな施設を全国各地にオープンしていく予定です。</p>
          <p>「パデルを通じて笑顔を創り、コミュニティーを創り、健康で元気な日本を創る！」このビジョンを胸に刻み、ブレずに信念と覚悟をもって真摯にパデル事業に取り組んで参ります。</p>





